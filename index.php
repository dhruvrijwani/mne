                errorMsg = $("#" + getCurrentId);
                // initialise plugin
                telInput.intlTelInput({
                    utilsScript: "../../build/js/utils.js",
                });
                var reset = function () {
                    telInput.removeClass("error");
                    //errorMsg.addClass("hide");
                };
                // on blur: validate
                if ($.trim(telInput.val())) {
                    if (telInput.intlTelInput("isValidNumber")) {
                        $("#" + getCurrentId).removeClass("error");
                        var getCode = $("#" + getCurrentId).intlTelInput("getSelectedCountryData").dialCode;
                        $("#countryCode" + getCurrentId).val(getCode);
                    } else {
                        $("#" + getCurrentId).val("");
                        $("#" + getCurrentId).addClass("error");
                    }
                }
                telInput.on("keyup change", reset);
                /*Error message only for mobile number and for microsite page ends here*/
